import {
  Body,
  Controller,
  Delete,
  Get,
  Param,
  Patch,
  Post,
  Query,
  Req,
  UseGuards,
} from '@nestjs/common';
import {
  ApiBearerAuth,
  ApiBody,
  ApiCreatedResponse,
  ApiForbiddenResponse,
  ApiOkResponse,
  ApiOperation,
  ApiParam,
  ApiQuery,
  ApiTags,
  ApiUnauthorizedResponse,
} from '@nestjs/swagger';
import { Role } from '@prisma/client';
import { JwtAuthGuard } from '../../../common/auth/guards/jwt-auth.guard';
import { RolesGuard } from '../../../common/auth/guards/roles.guard';
import { Roles } from '../../../common/auth/roles.decorator';
import { CreateInvoiceTemplateDto } from '../dto/create-invoice-template.dto';
import {
  DeleteInvoiceTemplateResponseDto,
  InvoiceTemplateResponseDto,
} from '../dto/invoice-template-response.dto';
import { InvoiceTemplateQueryDto } from '../dto/invoice-template-query.dto';
import { UpdateInvoiceTemplateDto } from '../dto/update-invoice-template.dto';
import { InvoiceTemplatesService } from '../services/invoice-templates.service';

